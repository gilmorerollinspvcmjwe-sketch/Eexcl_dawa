// 从渲染配置兜底读取单位中文名，避免展示层和运行时出现名称偏差。
function resolveUnitName(id: keyof typeof UNIT_CONFIGS, fallbackName: string) {
  return UNIT_CONFIGS[id]?.name ?? fallbackName;
}

// 保留旧 UI 需要的 lane 字段，但从新标准字段里稳定推导出来。
function toLegacyLane(layer: FantasyLaneLayer, role: FantasyLaneCombatRole): FantasyLaneLaneId {
  if (layer === 'air') return 'air';
  if (role === 'tank' || role === 'finisher') return 'front';
  if (role === 'fighter') return 'mid';
  return 'rear';
}

// 保留旧 UI 的 roleGroup，但不再让它成为运行时行为来源。
function toLegacyRoleGroup(role: FantasyLaneCombatRole): FantasyLaneRoleGroup {
  switch (role) {
    case 'tank':
      return 'tank';
    case 'fighter':
      return 'melee';
    case 'sniper':
      return 'ranged';
    case 'caster':
      return 'magic';
    case 'air_sup':
      return 'air';
    default:
      return 'finisher';
  }
}

// 保留旧展示尺寸标签，运行时仍只认 footprint。
function toLegacySize(footprint: FantasyLaneFootprint): FantasyLaneUnitSize {
  switch (footprint) {
    case 'small':
      return 'S';
    case 'medium':
      return 'M';
    case 'large':
      return 'L';
    default:
      return 'XL';
  }
}

// 统一把单位种子扩成完整定义，避免运行时再做默认字段兜底。
function createUnit(seed: UnitSeed): FantasyLaneUnitDefinition {
  const name = resolveUnitName(seed.id, seed.fallbackName);

  return {
    id: seed.id,
    name,
    shortName: seed.shortName,
    cost: seed.cost,
    pop: seed.pop,
    maxHp: seed.maxHp,
    armorHp: seed.armorHp,
    layer: seed.layer,
    rangeBand: seed.rangeBand,
    footprint: seed.footprint,
    role: seed.role,
    damageType: seed.damageType,
    damageProfile: seed.damageProfile,
    targetRule: seed.targetRule,
    moveSpeed: seed.moveSpeed,
    acquireRange: seed.acquireRange,
    preferredRange: seed.preferredRange,
    minimumRange: seed.minimumRange,
    attackIntervalMs: seed.attackIntervalMs,
    attackWindupMs: seed.attackWindupMs,
    attackAnimMs: seed.attackAnimMs,
    projectileSpeed: seed.projectileSpeed,
    collisionRadius: seed.collisionRadius,
    splashRadius: seed.splashRadius,
    damage: seed.damage,
    armorType: seed.armorType,
    icon: seed.icon,
    attackColor: seed.attackColor,
    cooldownMs: seed.cooldownMs,
    armorClass: seed.armorClass,
    roleGroup: toLegacyRoleGroup(seed.role),
    lane: toLegacyLane(seed.layer, seed.role),
    targetClass: seed.armorType,
    size: toLegacySize(seed.footprint),
    attackRange: seed.preferredRange,
    canAttackGround: seed.targetRule !== 'air_only',
    canAttackAir: seed.targetRule !== 'ground_only',
    tags: seed.tags,
    summary: seed.summary,
    signature: seed.signature,
  };
}

