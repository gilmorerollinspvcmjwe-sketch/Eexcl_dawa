// 扫描单个单位字段和值域，尽早拦住不完整或不合法的数据。
function validateUnit(unit: FantasyLaneUnitDefinition, seenIds: Set<string>) {
  const errors: string[] = [];

  if (seenIds.has(unit.id)) {
    errors.push(`${unit.id}: 单位 ID 重复`);
  }
  seenIds.add(unit.id);

  REQUIRED_STANDARD_FIELDS.forEach((field) => {
    if (unit[field] === undefined || unit[field] === null) {
      errors.push(`${unit.id}: 缺少标准字段 ${field}`);
    }
  });

  if (!LAYERS.includes(unit.layer)) errors.push(`${unit.id}: 非法 layer=${unit.layer}`);
  if (!RANGE_BANDS.includes(unit.rangeBand)) errors.push(`${unit.id}: 非法 rangeBand=${unit.rangeBand}`);
  if (!FOOTPRINTS.includes(unit.footprint)) errors.push(`${unit.id}: 非法 footprint=${unit.footprint}`);
  if (!ROLES.includes(unit.role)) errors.push(`${unit.id}: 非法 role=${unit.role}`);
  if (!DAMAGE_TYPES.includes(unit.damageType)) errors.push(`${unit.id}: 非法 damageType=${unit.damageType}`);
  if (!DAMAGE_PROFILES.includes(unit.damageProfile)) errors.push(`${unit.id}: 非法 damageProfile=${unit.damageProfile}`);
  if (!TARGET_RULES.includes(unit.targetRule)) errors.push(`${unit.id}: 非法 targetRule=${unit.targetRule}`);
  if (!ARMOR_TYPES.includes(unit.armorType)) errors.push(`${unit.id}: 非法 armorType=${unit.armorType}`);

  if (unit.cost <= 0) errors.push(`${unit.id}: cost 必须大于 0`);
  if (unit.pop <= 0) errors.push(`${unit.id}: pop 必须大于 0`);
  if (unit.maxHp <= 0) errors.push(`${unit.id}: maxHp 必须大于 0`);
  if (unit.damage <= 0) errors.push(`${unit.id}: damage 必须大于 0`);
  if (unit.moveSpeed <= 0) errors.push(`${unit.id}: moveSpeed 必须大于 0`);
  if (unit.attackIntervalMs <= 0) errors.push(`${unit.id}: attackIntervalMs 必须大于 0`);
  if (unit.attackWindupMs < 0) errors.push(`${unit.id}: attackWindupMs 不能小于 0`);
  if (unit.attackAnimMs <= 0) errors.push(`${unit.id}: attackAnimMs 必须大于 0`);
  if (unit.collisionRadius <= 0) errors.push(`${unit.id}: collisionRadius 必须大于 0`);
  if (unit.armorHp < 0) errors.push(`${unit.id}: armorHp 不能小于 0`);
  if (unit.minimumRange < 0) errors.push(`${unit.id}: minimumRange 不能小于 0`);
  if (unit.splashRadius < 0) errors.push(`${unit.id}: splashRadius 不能小于 0`);
  if (unit.acquireRange < unit.preferredRange) errors.push(`${unit.id}: acquireRange 不能小于 preferredRange`);
  if (unit.preferredRange < unit.minimumRange) errors.push(`${unit.id}: preferredRange 不能小于 minimumRange`);
  if (unit.rangeBand === 'melee' && unit.minimumRange !== 0) errors.push(`${unit.id}: 近战单位 minimumRange 必须为 0`);
  if (unit.rangeBand === 'ranged' && unit.minimumRange <= 0) errors.push(`${unit.id}: 远程单位 minimumRange 必须大于 0`);
  if (unit.rangeBand === 'ranged' && unit.projectileSpeed <= 0) errors.push(`${unit.id}: 远程单位 projectileSpeed 必须大于 0`);
  if (unit.rangeBand === 'melee' && unit.projectileSpeed !== 0) errors.push(`${unit.id}: 近战单位 projectileSpeed 必须为 0`);
  if (unit.damageProfile === 'single' && unit.splashRadius !== 0) errors.push(`${unit.id}: single 单位 splashRadius 必须为 0`);
  if (unit.damageProfile === 'aoe' && unit.splashRadius <= 0) errors.push(`${unit.id}: aoe 单位 splashRadius 必须大于 0`);
  if (unit.layer === 'air' && unit.armorType !== 'air') errors.push(`${unit.id}: 空军单位 armorType 必须为 air`);
  if (unit.layer === 'ground' && unit.armorType === 'air') errors.push(`${unit.id}: 地面单位 armorType 不能为 air`);
  if (unit.layer === 'air' && unit.lane !== 'air') errors.push(`${unit.id}: 空军单位 lane 必须稳定映射到 air`);
  if (unit.layer === 'ground' && unit.lane === 'air') errors.push(`${unit.id}: 地面单位 lane 不能映射到 air`);
  if (unit.targetRule === 'air_only' && !unit.canAttackAir) errors.push(`${unit.id}: air_only 单位必须能攻击空军`);
  if (unit.targetRule === 'ground_only' && !unit.canAttackGround) errors.push(`${unit.id}: ground_only 单位必须能攻击地面`);

  return errors;
}

// 对外暴露注册表校验，便于测试线和后续工具直接复用。
export function validateFantasyLaneUnitRegistry(units: FantasyLaneUnitDefinition[]) {
  const seenIds = new Set<string>();
  return units.flatMap((unit) => validateUnit(unit, seenIds));
}

